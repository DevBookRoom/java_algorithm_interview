class Solution {
    public int[] twoSum(int[] nums, int target) {
        int []answer=new int[2];
        for(int i=0;i<nums.length;i++){
            for(int j=i+1;j<nums.length;j++){
                if(target==nums[i]+nums[j]){
                    answer[0]=i;
                    answer[1]=j;
                    break;

                }

            }
        }
        return answer;


    }
}